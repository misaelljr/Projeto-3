package DTW3D;

public class Coords_Ext2 {
	
	private final double x, y, z;

    public Coords_Ext2(double x, double y, double z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public double getZ() {
        return z;
    }

}
